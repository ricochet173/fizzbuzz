class FizzBuzz

	def count
		
		1.upto(100) do |i|
			if i % 5 == 0 && i % 3 == 0
				puts "fizzbuzz"
			elsif i % 3 == 0
				puts "fizz"
			elsif i % 5 == 0
				puts "buzz"
			elsif 
				puts i

			end
		
		end

	end

end

